void main (void)
{
        /// Delay At Start
        for(U16 TimeStart=0; TimeStart<100; TimeStart++);

        /// Initialize WDT
	__init_WDT();
        /// Initialize General-purpose input/output
	__init_GPIO();
        /// Initialize Analog To Digital Converter
	__init_ADC();
        /// Initialize Power Managment
	__init_Power();
        /// Initialize Sleep
	__init_Sleep();
        
        /// Loop Machine
        //{
        while(1)
        {
                /// Proccess Power Machine
                POWER_Machine();
                
                /// Uninitialize Analog To Digital Converter
                __uninit_ADC();
                
                /// Goto Sleep Mode
                SLEEP_Run();

                /// Initialize Analog To Digital Converter
                __init_ADC();
                
        }
        //}

}
